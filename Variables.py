# Declare a variable x
x = 10

# Print the variable x
print(x)

# Output: 10


#You can also perform operations with x:


x = 10
y = 5

# Print the sum of x and y
print(x + y)

# Output: 15


#Or use x in a sentence:


x = "Aashman"

# Print a sentence with x
print("Hello, my name is " + x)

# Output: Hello, my name is Aashman
